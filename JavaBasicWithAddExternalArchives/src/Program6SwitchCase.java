
public class Program6SwitchCase {
	public static void main(String[] args) {
		
		int i = 2;
		switch (i) 
		{
		
		case 1: System.out.println("Mon-Day");
		break;
		
		case 2: System.out.println("Tues-Day");
		
		
		case 3: System.out.println("Wednes-Day");
		
		
		case 4: System.out.println("ThusDay");
		break;
		
    	case 5: System.out.println("FriDay");
		break;
		
     	case 6: System.out.println("SaturDay");
		break;
		
		case 7: System.out.println("SunDay");
		break;
		
		default: System.out.println("please enter a valid number in between 1-7");
			break;
	}
		}}
